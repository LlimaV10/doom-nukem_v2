int	ft_sqrt(int nb)
{
	int	x;

	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	x = 2;
	while (x * x <= nb)
	{
		if (x * x == nb)
		{
			return (x);
		}
		x++;
	}
	return (0);
}
