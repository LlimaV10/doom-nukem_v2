#include "libft.h"

t_list	*ft_lst_getn(t_list *lst, size_t n)
{
	size_t	i;

	if (lst == 0)
		return (0);
	i = 0;
	while ((i < n) && (lst != 0))
	{
		lst = lst->next;
		i++;
	}
	return (lst);
}
